/*
 * DirectFB key types (for advanced mapping)
 */
typedef enum {
    DIKT_UNICODE        = 0x0000UL,     /* Unicode 3.x character,(compatible to Latin-1) */
    DIKT_SPECIAL        = 0xF000UL,     /* Special key (e.g. Cursor Up or Menu) */
    DIKT_FUNCTION       = 0xF100UL,     /* Function key (F1 - Fn) */
    DIKT_MODIFIER       = 0xF200UL,     /* Modifier key */
    DIKT_LOCK           = 0xF300UL,     /* Lock key (e.g. CapsLock) */
    DIKT_DEAD           = 0xF400UL,     /* Dead key (e.g. dead grave) */
    DIKT_CUSTOM         = 0xF500UL,     /* Custom key (vendor specific) */
    DIKT_IDENTIFIER     = 0xF600UL,     /* DirectFB key identifier */
    DIKT_MTK            = 0xF700UL,     /* DirectFB MTK key identifier */
    DIKT_SPECIAL_2      = 0xFA00UL,     /* Special key customized by Hisense */
    DIKT_SPECIAL_3      = 0xFB00UL,     /* Special key customized by Hisense */
    DIKT_SMALLBOARD     = 0xE000UL        /* Ir small board*/
} DFBInputDeviceKeyType;
typedef enum{
    DIKT_DEVICE_KEYPAD        =    0,
    DIKT_DEVICE_MOUSE         =    1,
    DIKT_DEVICE_KEYBLUETOOTH  =    3,
    DIKT_DEVICE_IR            =    15,
    DIKT_DEVICE_KEYBOARD      =    17
} DFBInputDeviceIdType;

#define DFB_KEY(type,index)        (unsigned long int)((DIKT_##type) | (index))

#define DFB_KEY_TYPE(symbol)       ({(((symbol) & ~0xFFF) == 0xF000) ? ((((symbol) & 0xFF00) == DIKT_SPECIAL_2) ? DIKT_SPECIAL : (( ((symbol) & 0xFF00)== DIKT_SPECIAL_3) ? DIKT_SPECIAL:(symbol) & 0xFF00)) : DIKT_UNICODE ;})
#define DFB_KEY_INDEX(symbol)      ((symbol) & 0x00FF)

#define DFB_KEY_IS_ASCII(symbol)   ((symbol) < 128)

#define DFB_FUNCTION_KEY(n)        (DFB_KEY( FUNCTION, n ))
#define DFB_MODIFIER_KEY(i)        (DFB_KEY( MODIFIER, (1 << i) ))
#define DFB_CUSTOM_KEY(n)          (DFB_KEY( CUSTOM, n ))
#ifdef KEY_FROM_DFB /* MHF Linux - Bin Zhang */
#define DFB_MTK_KEY(n)             (DFB_KEY( MTK, n ))
#endif

#define DFB_LOWER_CASE(symbol)     (((symbol) >= 'A' && (symbol) <= 'Z') ?\
                                    ((symbol) | 0x20) : (symbol))
#define DFB_UPPER_CASE(symbol)     (((symbol) >= 'a' && (symbol) <= 'z') ?\
                                    ((symbol) & ~0x20) : (symbol))

